<h1>Aanmelden</h1>
<p>Je gebruikersnaam op deze website is het e-mailadres wat je bij het aanmaken van een account hebt opgegeven. Het wachtwoord is bij registratie per e-mail toegezonden en kan na aanmelden desgewenst gewijzigd worden.</p>

<h1>Account aanmaken</h1>
<p>Klik op de link <a href="?do=signup">Registreren</a> om een account voor deze website te maken. Je kunt alleen een account maken als dit is opengezet voor de organisatie waar je werkzaam bent. Om scenario's te kunnen opzoeken en bekijken is geen account nodig.</p>

<h1>Wachtwoord vergeten</h1>
<p>Als je je wachtwoord vergeten bent, klik dan op de link <a href="?do=lostpass">Wachtwoord vergeten</a>. Na opgeven van je e-mailadres kun je jezelf een nieuw wachtwoord laten toezenden. Na aanmelden met dit nieuwe wachtwoord kun je dit desgewenst zelf wijzigen in iets anders.</p>
