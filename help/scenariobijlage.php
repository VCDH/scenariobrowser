<h1>Scenariobijlagen</h1>
<p>Er kunnen bijlagen aan scenario's worden toegevoegd.</p>

<h1>Bestand uploaden</h1>
<p>Upload een bestand via de knop <em>Upload bestanden...</em> of sleep simpelweg de bestanden naar het browservenster. Er kunnen meerdere bestanden in &eacute;&eacute;n keer worden ge&uuml;pload. Bij nieuwe bestanden wordt standaard ingesteld dat deze alleen toegankelijk zijn voor ingelogde gebruikers van eigen organisatie.</p>

<h1>Betekenis van de pictogrammen</h1>
<p><span class="ui-icon ui-icon-locked"></span> Geeft aan dat het bestand alleen toegankelijk is voor ingelogde gebruikers van de eigen organisatie.<br>
<span class="ui-icon ui-icon-contact"></span> Geeft aan dat het bestand alleen toegankelijk is voor alle ingelogde gebruikers.<br>
<span class="ui-icon ui-icon-suitcase"></span> Geeft aan dat het bestand gearchiveerd is, het wordt dan niet in de scenarioweergave getoond, maar blijft wel bewaard.<br>
<span class="ui-icon ui-icon-trash"></span> Klik hierop om het bestand te verwijderen. In tegenstelling tot sommige andere programma's wordt hier wel gewoon gevraagd of je het zeker weet.<br>
<span class="ui-icon ui-icon-key"></span> Klik hierop om de toegangsinstellingen van het bestand te wijzigen of om het bestand te (de)archiveren.</p>
